c.customer_key,
c.first_name, 
c.last_name,
COUNT(DISTINCT order_number) AS total_orders
FROM gold.dim_customers c 
LEFT JOIN gold.fact_sales s
ON s.customer_key = c.customer_key
GROUP BY 
c.customer_key,
c.first_name, 
